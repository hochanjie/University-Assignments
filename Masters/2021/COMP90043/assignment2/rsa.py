def sign(M, n, d):
  s = 1
  while d > 0:
    if d % 2 == 1:
      s = (s * M) % n

    d = d // 2
    M = (M * M) % n

  return s

def verify(S, n, e, M):
  return sign(S, n, e) == M

def blindsign(M, x, n, e, d):
  Mb = ((x**e)* M) % n
  return sign(Mb, n, d)

